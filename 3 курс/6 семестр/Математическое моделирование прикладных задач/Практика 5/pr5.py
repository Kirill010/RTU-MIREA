U = 3  # Скорость истечения газов (км/с)
lambda_val = 0.1  # Коэффициент
V2_target = 8  # Целевая скорость второй ступени (км/с)
V1_target = V2_target / 2  # Целевая скорость первой ступени (км/с)

V3_target = 8  # Целевая скорость третьей ступени (км/с)
V2_target_3stage = V3_target / 2  # Целевая скорость второй ступени для трехступенчатой ракеты
V1_target_3stage = V2_target_3stage / 2  # Целевая скорость первой ступени для трехступенчатой ракеты


def calculate_two_stage_rocket(m0):
    """
    Функция для расчета массы ступеней двухступенчатой ракеты.
    Входные параметры: начальная масса ракеты (m0).
    Возвращает массы ступеней M1 и M2.
    """
    Mp = m0 / 20  # Полезная масса
    # Расчет массы первой ступени M1
    M1 = (m0 - Mp) / (1 + lambda_val)
    # Расчет массы второй ступени M2
    M2 = m0 - M1 - Mp
    return M1, M2


def calculate_three_stage_rocket(m0):
    """
    Функция для расчета массы ступеней трехступенчатой ракеты.
    Входные параметры: начальная масса ракеты (m0).
    Возвращает массы ступеней M1, M2 и M3.
    """
    Mp = m0 / 20  # Полезная масса
    # Расчет массы первой ступени M1
    M1 = (m0 - Mp) / (1 + lambda_val + lambda_val ** 2)
    # Расчет массы второй ступени M2
    M2 = lambda_val * M1
    # Расчет массы третьей ступени M3
    M3 = lambda_val * M2
    return M1, M2, M3


