def main(h):
    v = int(h)
    k1 = v & 0b11111111
    k2 = (v >> 8) & 0b11111111
    k4 = (v >> 17) & 0b11
    k5 = (v >> 19) & 0b1111111
    k6 = (v >> 26) & 0b111111
    d = (k2 << 24) | (k1 << 15) | (k4 << 13) | (k5 << 6) | k6
    return str(d)


print(main('1893924538')) # 4267517724
print(main('3808993577')) # 2702481528
print(main('483189354')) # 3795142215
print(main('3985518478')) # 793209979
